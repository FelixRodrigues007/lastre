//! Minimal CLI for deploying/interacting with the `ProofOfOrigin` contract through Odra CLI.

use lastro_contracts::proof_of_origin::ProofOfOrigin;
use odra::host::{HostEnv, NoArgs};
use odra_cli::{deploy::DeployScript, DeployedContractsContainer, DeployerExt, OdraCli};

/// Deploys the `ProofOfOrigin` contract and registers it in the Odra CLI container.
pub struct ProofOfOriginDeployScript;

impl DeployScript for ProofOfOriginDeployScript {
    fn deploy(
        &self,
        env: &HostEnv,
        container: &mut DeployedContractsContainer,
    ) -> Result<(), odra_cli::deploy::Error> {
        let _contract = ProofOfOrigin::load_or_deploy(
            env,
            NoArgs,
            container,
            350_000_000_000, // Default gas limit from the Odra template.
        )?;

        Ok(())
    }
}

/// Main entry point for the CLI generated by the Odra template.
pub fn main() {
    OdraCli::new()
        .about("CLI tool for the Lastro ProofOfOrigin contract")
        .deploy(ProofOfOriginDeployScript)
        .contract::<ProofOfOrigin>()
        .build()
        .run();
}
